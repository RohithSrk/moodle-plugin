/**
 * Saves a new instance of the newmodule into the database
 *
 * Given an object containing all the necessary data,
 * (defined by the form in mod_form.php) this function
 * will create a new instance and return the id number
 * of the new instance.
 *
 * @param stdClass $newmodule Submitted data from the form in mod_form.php
 * @param mod_newmodule_mod_form $mform The form instance itself (if needed)
 * @return int The id of the newly inserted newmodule record
 */
function checklist_add_instance(stdClass $checklist, mod_checklist_mod_form $mform = null) {
    global $DB;
    $checklist->timecreated = time();
    // You may have to add extra stuff in here.
    $checklist->id = $DB->insert_record('checklist', $checklist);
    return $checklist->id;
}
/**
 * Updates an instance of the newmodule in the database
 *
 * Given an object containing all the necessary data,
 * (defined by the form in mod_form.php) this function
 * will update an existing instance with new data.
 *
 * @param stdClass $newmodule An object from the form in mod_form.php
 * @param mod_newmodule_mod_form $mform The form instance itself (if needed)
 * @return boolean Success/Fail
 */
function checklist_update_instance(stdClass $checklist, mod_checklist_mod_form $mform = null) {
    global $DB;
    $checklist->timemodified = time();
    $checklist->id = $checklist->instance;
    // You may have to add extra stuff in here.
    $result = $DB->update_record('checklist', $checklist);
    return $result;
}

/**
 * Removes an instance of the newmodule from the database
 *
 * Given an ID of an instance of this module,
 * this function will permanently delete the instance
 * and any data that depends on it.
 *
 * @param int $id Id of the module instance
 * @return boolean Success/Failure
 */
function checklist_delete_instance($id) {
    global $DB;
    if (! $checklist = $DB->get_record('checklist', array('id' => $id))) {
        return false;
    }
    // Delete any dependent records here.
    $DB->delete_records('checklist', array('id' => $checklist->id));
    return true;
}

